    public static <T> void addToTable(List<T> list, DefaultTableModel model) {
        model.setRowCount(0);
        for (T t : list) {
            if (t instanceof Mathang) {
                Mathang mh = (Mathang) t;
                model.addRow(mh.toObjects());
            } else if (t instanceof Khachhang) {
                Khachhang kh = (Khachhang) t;
                model.addRow(kh.toObjects());
            }
        }
    }


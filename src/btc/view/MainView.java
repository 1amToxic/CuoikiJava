    private void btnAddMHActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddMHActionPerformed
        if (tfTenMH.getText().equals("") || tfGia.getText().equals("")) {

            try {
                throw new StringEqualNullException();

            } catch (StringEqualNullException ex) {
                showMessage("Khong duoc de trong");
            }
        } else {
            try {
                String tenH = tfTenMH.getText();
                String nhomH = cbbNhomH.getSelectedItem().toString();
                int gia = Integer.parseInt(tfGia.getText());
                Mathang mh = new Mathang(++maMH, tenH, nhomH, gia);
                listMH.add(mh);
                addToTable(listMH, modelMH);
            } catch (NumberFormatException ex) {
                showMessage("Gia phai la so");
            }
        }
    }//GEN-LAST:event_btnAddMHActionPerformed


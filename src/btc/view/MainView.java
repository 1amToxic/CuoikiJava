    private void btnAddKHActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddKHActionPerformed
        if (tfTenKH.getText().equals("") || tfDiachi.getText().equals("") || tfSDT.getText().equals("")) {

            try {
                throw new StringEqualNullException();

            } catch (StringEqualNullException ex) {
                showMessage("Khong duoc de trong");
            }
        } else {
            try {
                String tenKH = tfTenKH.getText();
                String nhomH = tfDiachi.getText();
                String sdt = tfSDT.getText();
                Khachhang kh = new Khachhang(++maKH, sdt, nhomH, sdt);
                listKH.add(kh);
                addToTable(listKH, modelKH);
            } catch (NumberFormatException ex) {
                showMessage("Gia phai la so");
            }
        }
    }//GEN-LAST:event_btnAddKHActionPerformed

